    private void GerarRelatorioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GerarRelatorioButtonActionPerformed
        // TODO add your handling code here:
        if(this.opcao.equals("Animais Por Cliente")) {
            String nome = RelatorioInput1.getText();
            
            if(nome.equals("")) {
                CamposVaziosLabel.setVisible(true);
                return;
            }
            
            List<Animal> animais = this.relatorioService.GeraAnimaisPorCliente(nome);
            CamposVaziosLabel.setVisible(false);
            RelatorioInput1.setText("");
            
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(animais);
            Map<String, Object> parametros = new HashMap<>();
            
            InputStream reportStream = getClass().getResourceAsStream("/relatorios/AnimaisPorCliente.jasper");
            
            try {
               JasperReport jasperReport = (JasperReport) JRLoader.loadObject(reportStream);
               JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametros, dataSource);
               JasperViewer.viewReport(jasperPrint, false);            
            } catch(Exception e) {
               System.out.println(e.getMessage());
            }

        } else if(this.opcao.equals("Consultas Por Veterinário")) {
            String veterinario = RelatorioInput1.getText();
            
            if(veterinario.equals("")) {
                CamposVaziosLabel.setVisible(true);
                return;
            }
            
            List<Consulta> consultas = this.relatorioService.GeraConsultasPorVeterinario(veterinario);
            CamposVaziosLabel.setVisible(false);
            RelatorioInput1.setText("");
            
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(consultas);
            Map<String, Object> parametros = new HashMap<>();
            
            InputStream reportStream = getClass().getResourceAsStream("/relatorios/ConsultasPorVeterinaio.jasper");
            
            try {
               JasperReport jasperReport = (JasperReport) JRLoader.loadObject(reportStream);
               JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametros, dataSource);
               JasperViewer.viewReport(jasperPrint, false);            
            } catch(Exception e) {
               System.out.println(e.getMessage());
            }
            
        } else if(this.opcao.equals("Consultas Por Período")) {
            String data_inicial = RelatorioInput1.getText();
            String data_final = RelatorioInput2.getText();
            
            if(data_inicial.equals("") || data_final.equals("")) {
                CamposVaziosLabel.setVisible(true);
                return;
            }
            
            this.relatorioService.GeraConsultasPorPeriodo(data_inicial, data_final);
            CamposVaziosLabel.setVisible(false);
            RelatorioInput1.setText("");
            RelatorioInput2.setText("");
        }
    }//GEN-LAST:event_GerarRelatorioButtonActionPerformed

