    public void atualizarCampos() {
        this.opcao = RelatorioSelect.getSelectedItem().toString();
        
        if(this.opcao.equals("Animais Por Cliente")) {
            RelatorioLabel1.setText("Cliente:");
            RelatorioLabel2.setVisible(false);
            RelatorioInput2.setVisible(false);

        } else if (this.opcao.equals("Consultas Por Veterinário")) {
            RelatorioLabel1.setText("Veterinário:");
            RelatorioLabel2.setVisible(false);
            RelatorioInput2.setVisible(false);
        } else if (this.opcao.equals("Consultas Por Período")) {
            RelatorioLabel1.setText("Data Inicial:");
            RelatorioLabel2.setText("Data Final:");

            RelatorioLabel2.setVisible(true);
            RelatorioInput2.setVisible(true);
        }

        revalidate();
        repaint();
    }
    
