    private void tabelaAnimaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAnimaisMouseClicked
        // TODO add your handling code here:
         // TODO add your handling code here:
        int linha = this.tabelaAnimais.getSelectedRow();
        if(linha == -1) return;
        
        this.idSelecionado = Integer.parseInt(this.tabelaAnimais.getValueAt(linha, 0).toString());
        
        NomeInput.setText(this.tabelaAnimais.getValueAt(linha, 1).toString());
        SexoInput.setText(this.tabelaAnimais.getValueAt(linha, 2).toString());
        IdadeInput.setText(this.tabelaAnimais.getValueAt(linha, 3).toString());
        ClienteIDInput.setText(this.tabelaAnimais.getValueAt(linha, 4).toString());
        EspecieInput.setText(this.tabelaAnimais.getValueAt(linha, 5).toString());
    }//GEN-LAST:event_tabelaAnimaisMouseClicked

    private void CadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarButtonActionPerformed
        // TODO add your handling code here:
        String nome = NomeInput.getText();
        String sexo = SexoInput.getText();
        int idade = Integer.parseInt(IdadeInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
        String especie = EspecieInput.getText();
        
        Animal animal = new Animal(nome, sexo, idade, cliente_id, especie);
        boolean result = animalController.cadastraAnimal(animal);
        
        carregaAnimais();
        JOptionPane.showMessageDialog(this, "Animal Cadastrado!");
        limparCampos();        
    }//GEN-LAST:event_CadastrarButtonActionPerformed

    private void AtualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarButtonActionPerformed
        // TODO add your handling code here:
         // TODO add your handling code here:
        
        String nome = NomeInput.getText();
        String sexo = SexoInput.getText();
        int idade = Integer.parseInt(IdadeInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
        String especie = EspecieInput.getText();
                
        Animal animal = new Animal(this.idSelecionado, nome, sexo, idade, cliente_id, especie);
        boolean result = animalController.atualizaAnimal(animal);
        
        carregaAnimais();
        JOptionPane.showMessageDialog(this, "Animal Atualizado!");
        limparCampos();
    }//GEN-LAST:event_AtualizarButtonActionPerformed

    private void ExcluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirButtonActionPerformed
        // TODO add your handling code here:
        int id = this.idSelecionado;
        String nome = NomeInput.getText();
        String sexo = SexoInput.getText();
        int idade = Integer.parseInt(IdadeInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
        String especie = EspecieInput.getText();
        
        Animal animal = new Animal(id, nome, sexo, idade, cliente_id, especie);
        boolean result = this.animalController.apagaAnimal(animal);
        
        if(result) {
            JOptionPane.showMessageDialog(this, "Animal Excluido!");
            carregaAnimais();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o animal!");
        }
        
        limparCampos();
    }//GEN-LAST:event_ExcluirButtonActionPerformed

