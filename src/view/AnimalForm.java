    public void carregaAnimais() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaAnimais.getModel();
        
        modelo.setRowCount(0);
        List<Animal> animais = this.animalController.listaAnimais();
        
        for(Animal a: animais) {
            modelo.addRow(new Object[] {
                a.getId(),
                a.getNome(),
                a.getSexo(),
                a.getIdade(),
                a.getClienteId(),
                a.getEspecie()
            });
        }
    }

