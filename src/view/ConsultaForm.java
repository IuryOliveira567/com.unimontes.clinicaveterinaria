    private void limparCampos() {
        HoraConsultaInput.setText("");
        DataConsultaInput.setText("");
        DescricaoInput.setText("");
        AnimalIDInput.setText("");
        VeterinarioIDInput.setText("");
        ClienteIDInput.setText("");
    }
    
    public void carregaConsultas() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaConsultas.getModel();
        
        modelo.setRowCount(0);
        List<Consulta> consultas = this.consultaController.listaConsultas();
        
        for(Consulta c: consultas) {
            modelo.addRow(new Object[] {
                c.getId(),
                c.getDataConsulta(),
                c.getHoraConsulta(),
                c.getDescricao(),
                c.getAnimalId(),
                c.getVeterinarioId(),
                c.getClienteId()
            });
        }
    }
