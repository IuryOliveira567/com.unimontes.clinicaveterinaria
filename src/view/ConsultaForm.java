    private void tabelaConsultasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaConsultasMouseClicked
        // TODO add your handling code here:
        int linha = this.tabelaConsultas.getSelectedRow();
        if(linha == -1) return;
        
        this.idSelecionado = Integer.parseInt(this.tabelaConsultas.getValueAt(linha, 0).toString());
        
        DataConsultaInput.setText(this.tabelaConsultas.getValueAt(linha, 1).toString());
        HoraConsultaInput.setText(this.tabelaConsultas.getValueAt(linha, 2).toString());
        DescricaoInput.setText(this.tabelaConsultas.getValueAt(linha, 3).toString());
        AnimalIDInput.setText(this.tabelaConsultas.getValueAt(linha, 4).toString());
        VeterinarioIDInput.setText(this.tabelaConsultas.getValueAt(linha, 5).toString());
        ClienteIDInput.setText(this.tabelaConsultas.getValueAt(linha, 6).toString());
    }//GEN-LAST:event_tabelaConsultasMouseClicked

    private void AgendarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgendarButtonActionPerformed
        // TODO add your handling code here:
        String data = DataConsultaInput.getText();
        String hora = HoraConsultaInput.getText();
        String descricao = DescricaoInput.getText();
        int animal_id = Integer.parseInt(AnimalIDInput.getText());
        int veterinario_id = Integer.parseInt(VeterinarioIDInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
        
        Consulta consulta = new Consulta(data, hora, descricao, animal_id, veterinario_id, cliente_id);
        boolean result = consultaController.cadastraConsulta(consulta);
        
        carregaConsultas();
        JOptionPane.showMessageDialog(this, "Consulta Cadastrada!");
        limparCampos();        
    }//GEN-LAST:event_AgendarButtonActionPerformed

    private void AtualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarButtonActionPerformed
        // TODO add your handling code here:
        String data = DataConsultaInput.getText();
        String hora = HoraConsultaInput.getText();
        String descricao = DescricaoInput.getText();
        
        int animal_id = Integer.parseInt(AnimalIDInput.getText());
        int veterinario_id = Integer.parseInt(VeterinarioIDInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
                
        Consulta consulta = new Consulta(this.idSelecionado, data, hora, descricao, animal_id, veterinario_id, cliente_id);
        boolean result = consultaController.atualizaConsulta(consulta);
        
        carregaConsultas();
        JOptionPane.showMessageDialog(this, "Consulta Atualizada!");
        limparCampos();
    }//GEN-LAST:event_AtualizarButtonActionPerformed

    private void ExcluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirButtonActionPerformed
        // TODO add your handling code here:
        int id = this.idSelecionado;
        String data = DataConsultaInput.getText();
        String hora = HoraConsultaInput.getText();
        String descricao = DescricaoInput.getText();
        int animal_id = Integer.parseInt(AnimalIDInput.getText());
        int veterinario_id = Integer.parseInt(VeterinarioIDInput.getText());
        int cliente_id = Integer.parseInt(ClienteIDInput.getText());
        
        Consulta consulta = new Consulta(id, data, hora, descricao, animal_id, veterinario_id, cliente_id);
        boolean result = this.consultaController.apagaConsulta(consulta);
        
        if(result) {
            JOptionPane.showMessageDialog(this, "Consulta excluida!");
            carregaConsultas();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir consulta!");
        }
        
        limparCampos();
    }//GEN-LAST:event_ExcluirButtonActionPerformed

