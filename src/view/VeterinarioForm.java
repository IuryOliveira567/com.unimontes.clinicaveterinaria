    private void tabelaVeterinariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaVeterinariosMouseClicked
        // TODO add your handling code here:
          // TODO add your handling code here:
        int linha = this.tabelaVeterinarios.getSelectedRow();
        if(linha == -1) return;
        
        this.idSelecionado = Integer.parseInt(this.tabelaVeterinarios.getValueAt(linha, 0).toString());
        NomeInput.setText(this.tabelaVeterinarios.getValueAt(linha, 1).toString());
        CRMVInput.setText(this.tabelaVeterinarios.getValueAt(linha, 2).toString());
        TelefoneInput.setText(this.tabelaVeterinarios.getValueAt(linha, 3).toString());
    }//GEN-LAST:event_tabelaVeterinariosMouseClicked

    private void CadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarButtonActionPerformed
        // TODO add your handling code here:
        
        if(verificaCampos()) {
            LabelFormError.setVisible(true);
        } else {
            String nome = NomeInput.getText();
            String crmv = CRMVInput.getText();
            String telefone = TelefoneInput.getText();
        
            Veterinario veterinario = new Veterinario(nome, crmv, telefone);
            boolean result = veterinarioController.cadastraVeterinario(veterinario);
        
            if(result) {
                carregaVeterinarios();
                JOptionPane.showMessageDialog(this, "Veterinario Cadastrado!");
                limparCampos(); 
            } else {
               JOptionPane.showMessageDialog(this, "Veterinario já Existe!");
            }
            
            LabelFormError.setVisible(false);

        }
    }//GEN-LAST:event_CadastrarButtonActionPerformed

    private void AtualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarButtonActionPerformed
        // TODO add your handling code here:
         // TODO add your handling code here:
        if(verificaCampos()) {
            LabelFormError.setVisible(true);
        } else {
            String nome = NomeInput.getText();
            String crmv = CRMVInput.getText();
            String telefone = TelefoneInput.getText();
                
            Veterinario veterinario = new Veterinario(this.idSelecionado, nome, crmv, telefone);
            boolean result = veterinarioController.atualizaVeterinario(veterinario);
        
            carregaVeterinarios();
            JOptionPane.showMessageDialog(this, "Veterinario Atualizado!");
            limparCampos();
            LabelFormError.setVisible(false);
        }
    }//GEN-LAST:event_AtualizarButtonActionPerformed

    private void ExcluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirButtonActionPerformed
        // TODO add your handling code here:
        if(verificaCampos()) {
            LabelFormError.setVisible(true);
        } else {
            int id = this.idSelecionado;
            String nome = NomeInput.getText();
            String crmv = CRMVInput.getText();
            String telefone = TelefoneInput.getText();
        
            Veterinario veterinario = new Veterinario(id, nome, crmv, telefone);
            boolean result = this.veterinarioController.apagaVeterinario(veterinario);
        
            if(result) {
                JOptionPane.showMessageDialog(this, "Veterinario Excluido!");
                carregaVeterinarios();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o Veterinario!");
            }
        
            limparCampos();
            LabelFormError.setVisible(false);
        }
    }//GEN-LAST:event_ExcluirButtonActionPerformed

