    public void carregaVeterinarios() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaVeterinarios.getModel();
        
        modelo.setRowCount(0);
        List<Veterinario> veterinarios = this.veterinarioController.listaVeterinarios();
        
        for(Veterinario v: veterinarios) {
            modelo.addRow(new Object[] {
                v.getId(),
                v.getNome(),
                v.getCrmv(),
                v.getTelefone()
            });
        }
    }

    private void limparCampos() {
        NomeInput.setText("");
        CRMVInput.setText("");
        TelefoneInput.setText("");
    }
    
