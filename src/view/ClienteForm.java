    private void CadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarButtonActionPerformed
        // TODO add your handling code here:
        String nome = NomeInput.getText();
        String cpf = CPFInput.getText();
        String telefone = TelefoneInput.getText();
        String email = EmailInput.getText();
        
        if(!verificaCPF(cpf)) {
            Cliente cliente = new Cliente(nome, cpf, telefone, email);
            boolean result = clienteController.cadastraCliente(cliente);
        
             carregaClientes();
             JOptionPane.showMessageDialog(this, "Cliente Cadastrado!");
             limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "CPF já cadastrado");
        }
    }//GEN-LAST:event_CadastrarButtonActionPerformed

    private void AtualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarButtonActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        
        String nome = NomeInput.getText();
        String cpf = CPFInput.getText();
        String telefone = TelefoneInput.getText();
        String email = EmailInput.getText();
                
        Cliente cliente = new Cliente(this.idSelecionado, nome, cpf, telefone, email);
        boolean result = clienteController.atualizaCliente(cliente);
        
        carregaClientes();
        JOptionPane.showMessageDialog(this, "Cliente Atualizado!");
        limparCampos();
    }//GEN-LAST:event_AtualizarButtonActionPerformed

    private void tabelaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClientesMouseClicked
        // TODO add your handling code here:
        int linha = this.tabelaClientes.getSelectedRow();
        if(linha == -1) return;
        
        this.idSelecionado = Integer.parseInt(this.tabelaClientes.getValueAt(linha, 0).toString());
        
        NomeInput.setText(this.tabelaClientes.getValueAt(linha, 1).toString());
        CPFInput.setText(this.tabelaClientes.getValueAt(linha, 2).toString());
        TelefoneInput.setText(this.tabelaClientes.getValueAt(linha, 3).toString());
        EmailInput.setText(this.tabelaClientes.getValueAt(linha, 4).toString()); 
    }//GEN-LAST:event_tabelaClientesMouseClicked

