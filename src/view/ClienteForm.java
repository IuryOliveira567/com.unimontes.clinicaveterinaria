    public boolean verificaCPF(String cpf) {
        for(int i = 0; i < this.tabelaClientes.getRowCount(); i++) {
            String cpfTabela = tabelaClientes.getValueAt(i, 2).toString(); 
            if(cpfTabela.equals(cpf)) {
                return true;
            }
        }
        
        return false;
    }
    
    private void limparCampos() {
        
        NomeInput.setText("");
        CPFInput.setText("");
        TelefoneInput.setText("");
        EmailInput.setText("");
    }
            
